package info.freelibrary.openjpeg;

public class EncodingException extends Exception {

	/**
	 * Generated <code>serialVersionUID</code>.
	 */
	private static final long serialVersionUID = 1008881918601779654L;

	public EncodingException() {
		super();
	}

	public EncodingException(String aMessage) {
		super(aMessage);
	}
	
	public EncodingException(String aMessage, Throwable aThrowable) {
		super(aMessage, aThrowable);
	}
	
	public EncodingException(Throwable aThrowable) {
		super(aThrowable);
	}

}
