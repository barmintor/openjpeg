package info.freelibrary.openjpeg;

public class DecodingException extends Exception {

	/**
	 * Generated <code>serialVersionUID</code>.
	 */
	private static final long serialVersionUID = -7888180067328883967L;

	public DecodingException() {
		super();
	}

	public DecodingException(String aMessage) {
		super(aMessage);
	}
	
	public DecodingException(String aMessage, Throwable aThrowable) {
		super(aMessage, aThrowable);
	}
	
	public DecodingException(Throwable aThrowable) {
		super(aThrowable);
	}

}
